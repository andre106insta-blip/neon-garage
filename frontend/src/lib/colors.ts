export function colorToHex(input?: string | null): string {
  if (!input) return "#c0c4c8"; // нейтральный серебристый по умолчанию
  const key = input.trim().toLowerCase();
  if (MAP[key]) return MAP[key];
  // частичное совпадение для составных названий вроде "тёмно красный металлик"
  for (const k of Object.keys(MAP)) {
    if (key.includes(k)) return MAP[k];
  }
  // hex как есть, если введён напрямую
  if (/^#?[0-9a-f]{3,8}$/i.test(key)) return key.startsWith("#") ? key : `#${key}`;
  return "#c0c4c8";
}
